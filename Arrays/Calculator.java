package EvozonJavaBasics.Arrays;

public class Calculator {
    public int addNumbers(int a, int b) {
        return a + b;
    }

    public int addNumbers(int a, int b, int c) {
        return a + b + c;
    }

    public float addNumbers(float a, float b) {
        return a + b;
    }

    public int subtractNumbers(int a, int b) {
        return a - b;
    }  

    public long subtractNumbers(long a, long b) {
        return a - b;
    } 

    public float subtractNumbers(float a, float b) {
        return a - b;
    } 

    public int multiplyNumbers(int a, int b) {
        return a * b;
    } 

    public int multiplyNumbers(int a, int b, int c) {
        return a * b * c;
    } 

    public float multiplyNumbers(float a, float b) {
        return a * b;
    } 

    public float divideNumbers(int a, int b) {
        return (float) a / b;
    }   

    public float divideNumbers(float a, float b) {
        return a / b;
    }  

    public double divideNumbers(long a, long b) {
        return (double) a / b;
    }  
}
